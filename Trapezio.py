print("Calculo da area de um trapézio")
base_maior = int(input("digite o valor da base maior: "))
base_menor = int(input("digite o valor da  base menor: "))
altura = int(input("digite o valor da altura: "))

A = int((base_maior + base_menor) * altura) / 2

print("O valor do da area do trapézio é :", A)
